var wishlist = JSON.parse(localStorage.getItem("wishlistdb")) || [];
var cartlist = JSON.parse(localStorage.getItem("cartlistdb")) || [];

// Pagination settings
const itemsPerPage = 20;
let currentPage = 1;
let filteredData = [...allProductData]; // Use this to store filtered results

// Initialize and display data
displayData(filteredData, currentPage);
updateWishlistCartLength();

// Event listeners for filters and pagination
document.querySelector("#sortByGender").addEventListener("change", debounce(applyFiltersAndSort, 300));
document.querySelector("#filterByType").addEventListener("change", debounce(applyFiltersAndSort, 300));
document.querySelector("#sortByPrice").addEventListener("change", debounce(applyFiltersAndSort, 300));
document.querySelector("#prevPage").addEventListener("click", () => changePage(-1));
document.querySelector("#nextPage").addEventListener("click", () => changePage(1));

function updateWishlistCartLength() {
    document.querySelector("#wishlistLen").textContent = wishlist.length;
    document.querySelector("#cartLen").textContent = cartlist.length;
}

function displayData(arr, page) {
    const container = document.querySelector("#allProductDiv");
    container.innerHTML = "";

    // Calculate the start and end index for pagination
    const startIndex = (page - 1) * itemsPerPage;
    const endIndex = Math.min(startIndex + itemsPerPage, arr.length);
    const paginatedData = arr.slice(startIndex, endIndex);

    paginatedData.forEach(function (element) {
        const div = document.createElement("div");
        
        const img = document.createElement("img");
        img.setAttribute("src", element.imgURL);
        
        const price = document.createElement("p");
        price.textContent = "Rs. " + element.price;
        price.setAttribute("class", "price");
        
        const symDiv = document.createElement("div");
        symDiv.innerHTML = '<i class="far fa-heart"></i>';
        symDiv.setAttribute("class", "wishlistSymbol");
        symDiv.addEventListener("click", function() {
            toggleWishlist(element, symDiv);
        });
        
        const cartSymDiv = document.createElement("div");
        cartSymDiv.innerHTML = '<i class="fas fa-cart-plus"></i>';
        cartSymDiv.setAttribute("class", "cartSym");
        cartSymDiv.addEventListener("click", function() {
            addtocart(element, cartSymDiv);
        });
        
        const name = document.createElement("p");
        name.textContent = element.name;
        
        const type = document.createElement("p");
        type.textContent = element.type;
        
        div.append(img, price, symDiv, cartSymDiv, name, type);
        container.append(div);
    });

    // Update pagination controls
    updatePaginationControls(arr.length);
}

function updatePaginationControls(totalItems) {
    const totalPages = Math.ceil(totalItems / itemsPerPage);
    document.querySelector("#prevPage").disabled = currentPage === 1;
    document.querySelector("#nextPage").disabled = currentPage === totalPages;
    document.querySelector("#pageNumber").textContent = `Page ${currentPage}`;
}

function applyFiltersAndSort() {
    let filteredArr = [...allProductData];
    const gender = document.querySelector("#sortByGender").value;
    const type = document.querySelector("#filterByType").value;
    const sortValue = document.querySelector("#sortByPrice").value;

    if (gender) {
        filteredArr = filteredArr.filter(element => element.gender.toLowerCase() === gender.toLowerCase());
    }

    if (type) {
        filteredArr = filteredArr.filter(element => element.type === type);
    }

    if (sortValue === "lowToHigh") {
        filteredArr.sort((a, b) => Number(a.price) - Number(b.price));
    } else if (sortValue === "highToLow") {
        filteredArr.sort((a, b) => Number(b.price) - Number(a.price));
    }

    filteredData = filteredArr; // Update the filtered data
    currentPage = 1; // Reset to the first page
    displayData(filteredData, currentPage);
}

function addtocart(element, cartSymDiv) {
    cartSymDiv.innerHTML = '<i class="fas fa-shopping-bag"></i>';
    if (!cartlist.some(item => item.imgURL === element.imgURL)) {
        cartlist.push(element);
        localStorage.setItem('cartlistdb', JSON.stringify(cartlist));
        updateWishlistCartLength();
    }
}

function toggleWishlist(element, symDiv) {
    if (wishlist.some(item => item.imgURL === element.imgURL)) {
        symDiv.innerHTML = '<i class="far fa-heart"></i>';
        wishlist = wishlist.filter(item => item.imgURL !== element.imgURL);
    } else {
        symDiv.innerHTML = '<i class="fas fa-heart"></i>';
        wishlist.push(element);
    }
    localStorage.setItem('wishlistdb', JSON.stringify(wishlist));
    updateWishlistCartLength();
}

function changePage(direction) {
    const totalItems = filteredData.length;
    const totalPages = Math.ceil(totalItems / itemsPerPage);

    currentPage = Math.max(1, Math.min(currentPage + direction, totalPages));
    displayData(filteredData, currentPage);
}

// Debounce function to limit the rate of function calls
function debounce(func, wait) {
    let timeout;
    return function(...args) {
        clearTimeout(timeout);
        timeout = setTimeout(() => func.apply(this, args), wait);
    };
}



